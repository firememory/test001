package com.fortune.trading.entity;

/**
 * <code>Funds</code>
 *
 * @author Colin, Jimmy
 * @since Tebon Trading v0.0.1
 */
public class Funds {
	private String acc;
	private String bz;
	private String fundsBal;
	private String freezebal;
	private String availFunds;
	private String adviFunds;
	private String marketVal;
	private String totalAssert;
	
	public String getAcc() {
		return acc;
	}
	public void setAcc(String acc) {
		this.acc = acc;
	}
	public String getBz() {
		return bz;
	}
	public void setBz(String bz) {
		this.bz = bz;
	}
	public String getFundsBal() {
		return fundsBal;
	}
	public void setFundsBal(String fundsBal) {
		this.fundsBal = fundsBal;
	}
	public String getFreezebal() {
		return freezebal;
	}
	public void setFreezebal(String freezebal) {
		this.freezebal = freezebal;
	}
	public String getAvailFunds() {
		return availFunds;
	}
	public void setAvailFunds(String availFunds) {
		this.availFunds = availFunds;
	}
	public String getAdviFunds() {
		return adviFunds;
	}
	public void setAdviFunds(String adviFunds) {
		this.adviFunds = adviFunds;
	}
	public String getMarketVal() {
		return marketVal;
	}
	public void setMarketVal(String marketVal) {
		this.marketVal = marketVal;
	}
	public String getTotalAssert() {
		return totalAssert;
	}
	public void setTotalAssert(String totalAssert) {
		this.totalAssert = totalAssert;
	}
		
}
