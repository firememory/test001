package org.xvolks.jnative.com.typebrowser.business;

import java.io.IOException;

public class TypeLibNotFoundException extends IOException {

	public TypeLibNotFoundException() {
	}

	public TypeLibNotFoundException(String arg0) {
		super(arg0);
	}

	public TypeLibNotFoundException(Throwable arg0) {
		super(arg0);
	}

	public TypeLibNotFoundException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
